export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.featured).slice(0, 8);
}

export function getTrendingProducts(): Product[] {
  return products.filter((p) => p.trending).slice(0, 8);
}

export function getProductsByArtist(artistId: string): Product[] {
  return products.filter((p) => p.artistId === artistId);
}

export function getProductsByCategory(categoryId: string): Product[] {
  return products.filter((p) => p.category === categoryId);
}

export function getRelatedProducts(product: Product, limit = 4): Product[] {
  return products
    .filter((p) => p.id !== product.id && (p.category === product.category || p.artistId === product.artistId))
    .slice(0, limit);
}
